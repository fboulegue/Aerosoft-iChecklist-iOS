function openKatana_1Introduction()
{
	var networkState = navigator.connection.type;
	if (networkState == Connection.NONE)
    {
        window.location = "../../fuctions_pages/offline_checklist_katana.html";
        console.log('buy-katana-offline');
    }
	else
	{
		var ref = window.open('http://fboulegue.pf-control.de/aerosoftapp/katana/1Introduction.pdf', '_blank', 'location=no,enableViewportScale=yes');
        console.log('openKatana_1Introduction');
	}
}
